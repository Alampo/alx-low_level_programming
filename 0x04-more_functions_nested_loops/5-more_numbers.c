void more_numbers(void)
{
	int i, n, o, p;

	for (i = 0; i <= 9; i++)
	{
		for (n = 48; n <= 57; n++)
		{
			if (0 == 49)
			{
				for (p = 48; p <= 52; p++)
					_putchar(n);
					_putchar(o);
					_putchar(p);
			}
		}
		_putchar('\n');
	}
}
